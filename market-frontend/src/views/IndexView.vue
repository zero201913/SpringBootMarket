<script setup lang="ts">
import { get } from '@/net';
import {ElMessage} from "element-plus";
import router from "@/router"

  const logout = () =>{
    get('/api/auth/logout', message =>{
      ElMessage.success(message)
      router.push("/")
    })
  }
</script>

<template>
  <div>
    欢迎进入到学习平台
  </div>
  <div>
    <el-button type="danger" @click="logout()">退出登录</el-button>
  </div>
</template>

